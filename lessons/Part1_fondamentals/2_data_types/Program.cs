using System;

namespace data_types
{
    class Program
    {
        static void Main(string[] args)
        {
            int myNum = 5; //Integer value
            long myNum = 15000000000L; //Long integer value
            float myFloatNum = 2.3F; //Single-precision decimal value ('F' optional)
            double myDoubleNum = 5.99D; //Double-precision decimal value ('D' optional)
            char myLetter = 'D'; //Single character
            bool myBool = true; //Boolean value (true/false)
            string myText = "Hello"; //String value

            //Scientific notation
            float f1 = 35e3F;
            double d1 = 12E4D;
        }
    }
}
